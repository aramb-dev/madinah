// --- ENUMS AND INTERFACES (as defined in the schema) ---

export enum WordType {
  Noun = 'noun',
  Verb = 'verb',
  Adjective = 'adjective',
  Adverb = 'adverb',
  Preposition = 'preposition',
  Pronoun = 'pronoun',
  Conjunction = 'conjunction',
  Particle = 'particle',
  Expression = 'expression',
}

export interface Example {
  arabic: string;
  english: string;
  notes?: string;
}

export interface VerbConjugation {
  past: {
    singular: {
      first: string;
      secondMasculine: string;
      secondFeminine: string;
      thirdMasculine: string;
      thirdFeminine: string;
    };
    plural: {
      first: string;
      second: string;
      thirdMasculine: string;
      thirdFeminine: string;
    };
  };
  present: {
    singular: {
      first: string;
      secondMasculine: string;
      secondFeminine: string;
      thirdMasculine: string;
      thirdFeminine: string;
    };
    plural: {
      first: string;
      second: string;
      thirdMasculine: string;
      thirdFeminine: string;
    };
  };
  imperative?: {
    singular: {
      masculine: string;
      feminine: string;
    };
    plural: string;
  };
  verbal_noun?: string;
  active_participle?: string;
  passive_participle?: string;
}

export interface VocabularyItem {
  id: string;
  word: string;
  transliteration: string;
  translation: {
    en: string;
  };
  type: WordType;
  root?: string;
  plural?: string;
  gender?: 'masculine' | 'feminine' | 'neuter';
  conjugation?: VerbConjugation;
  definition: string;
  examples: Example[];
  notes?: string;
  difficulty: 'beginner' | 'intermediate' | 'advanced';
  tags: string[];
  relatedWords?: string[];
}

export interface VocabularyList {
  id: string;
  title: {
    ar: string;
    en: string;
  };
  description: {
    arabic: string;
    english: string;
  };
  bookId?: string;
  lessonId?: string;
  items: VocabularyItem[]; // Adapted for a self-contained file
  level: 'beginner' | 'intermediate' | 'advanced';
  tags: string[];
  order: number;
}

