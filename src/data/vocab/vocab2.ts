/**
 * Defines the structure for a single vocabulary item.
 */
export interface VocabularyItem {
  id: string; // Unique identifier (e.g., "b2l1v1")
  lesson: number; // Lesson number
  word: string; // The Arabic word
  transliteration: string; // Latin character transliteration
  translation: {
    en: string; // English translation
  };
  type: WordType; // Part of speech (noun, verb, etc.)
  root?: string; // Arabic root (if applicable)
  plural?: string; // Plural form for nouns
  gender?: 'masculine' | 'feminine' | 'neuter' | 'dual'; // Gender information
  definition: string; // Detailed definition in English
  examples: Example[]; // Usage examples (currently empty as per source)
  notes?: string; // Additional notes or special cases
  difficulty: 'beginner' | 'intermediate' | 'advanced';
  tags: string[]; // For categorization and filtering
  relatedWords?: string[]; // IDs of related vocabulary items
}

/**
 * Enum for the different types of words (parts of speech).
 */
export enum WordType {
  Noun = 'noun',
  Verb = 'verb',
  Adjective = 'adjective',
  Adverb = 'adverb',
  Preposition = 'preposition',
  Pronoun = 'pronoun',
  Conjunction = 'conjunction',
  Particle = 'particle',
  Expression = 'expression',
  Number = 'number',
  ProperNoun = 'proper noun',
}

/**
 * Interface for usage examples.
 */
export interface Example {
  arabic: string;
  english: string;
  notes?: string;
}

